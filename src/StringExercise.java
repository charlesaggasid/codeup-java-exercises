public class StringExercise {
    public static void main(String[] args) {
        String StringOutput = "We don't need no education\nWe don't need no thought control";
        System.out.println(StringOutput);

        StringOutput = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(StringOutput);

        StringOutput = "In windows, the main drive is usually C:\\";
        System.out.println(StringOutput);

        StringOutput = "I can do backslashes \\, double backslashes \\\\, and the amazing triple backslash \\\\\\!";
        System.out.println(StringOutput);

    }
}
